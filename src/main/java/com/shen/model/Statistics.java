package com.shen.model;

public class Statistics {
    private String statisticsName;
    private String unattendenceCount;

    public String getStatisticsName() {
        return statisticsName;
    }

    public String getUnattendenceCount() {
        return unattendenceCount;
    }

    public void setStatisticsName(String statisticsName) {
        this.statisticsName = statisticsName;
    }

    public void setUnattendenceCount(String unattendenceCount) {
        this.unattendenceCount = unattendenceCount;
    }
}
